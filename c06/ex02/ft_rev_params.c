#include <unistd.h>

void	ft_put_str(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
}

int	main(int argc, char *argv[])
{
	int	i;

	i = argc - 1;
	while (i > 0)
	{
		ft_put_str(argv[i]);
		ft_put_str("\n");
		i--;
	}
	return (0);
}
