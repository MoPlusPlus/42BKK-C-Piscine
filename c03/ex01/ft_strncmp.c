int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int		i;

	i = 0;
	while (i < n)
	{
		if (*s1 < *s2)
			return (-1);
		else if (*s1 > *s2)
			return (1);
		if (*s1)
			s1++;
		if (*s2)
			s2++;
		i++;
	}
	return (0);
}
