int	ft_strcmp(char *s1, char *s2)
{
	int		i;
	char	gap;

	i = 0;
	while (s1[i] != '\0')
	{
		if (s2[i] == '\0')
			return (1);
		gap = s1[i] - s2[i];
		if (gap < 0)
			return (-1);
		else if (gap > 0)
			return (1);
		i++;
	}
	if (s2[i] != '\0')
		return (-1);
	return (0);
}
